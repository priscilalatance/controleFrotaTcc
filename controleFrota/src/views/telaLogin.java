    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String user = usuario.getText();        
        String password = senha.getText();
        
        //Compara se está vazio o campo usuario e senha
        if(user.equals("") || password.equals("")){
            //Retorna uma tela com a mensagem de erro
            JOptionPane.showMessageDialog(this, "Preencha os Campos", "Login", 0,
                    new ImageIcon(getClass().getResource("/Imagem/info.png")));
            
        }else{
            //chama o método que faz o login
           Logar(user, password);
        }               
    }//GEN-LAST:event_btnLoginActionPerformed

    
