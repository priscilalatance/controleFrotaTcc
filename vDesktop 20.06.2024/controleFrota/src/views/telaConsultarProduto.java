    private void btnConsultarDescrProdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarDescrProdActionPerformed
        CadastroItemDao dao = new CadastroItemDao();
        String descricaoConsulta = txtDescricaoProd.getText();
        boolean status = dao.conectar();
        if (status) {
            List<CadastroItem> itensEncontrados = dao.consultarPorDescricao(descricaoConsulta);

            if (itensEncontrados.isEmpty()) {
                lblConsDescProd.setText("Não há cadastro com a descrição fornecida.");
            } else {
                DefaultTableModel model = new DefaultTableModel();
                model.addColumn("CÓD.");
                model.addColumn("DESCRIÇÃO PRODUTO");

                for (CadastroItem item : itensEncontrados) {
                    model.addRow(new Object[]{
                        item.getIdCodigo(),
                        item.getDescricaoItem(),});
                }

                tblCadastroItem.setModel(model);

                tblCadastroItem.getColumnModel().getColumn(0).setPreferredWidth(100);
                tblCadastroItem.getColumnModel().getColumn(1).setPreferredWidth(800);
            }
        } else {
            lblConsDescProd.setText("Erro na conexão com o banco de dados");
        }

        dao.desconectar();

    }//GEN-LAST:event_btnConsultarDescrProdActionPerformed


