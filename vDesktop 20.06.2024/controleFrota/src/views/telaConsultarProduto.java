    private void tblCadastroItemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCadastroItemMouseClicked
        // Obtém a linha selecionada
        int row = tblCadastroItem.getSelectedRow();
        // Obtém o valor da primeira coluna da linha selecionada e converte para Integer
        Integer selectedValue = Integer.parseInt(tblCadastroItem.getModel().getValueAt(row, 0).toString());
        // Cria uma instância de guardaIdItem
        guardaIdItem itemGuardado = new guardaIdItem();
        // Armazena o valor na instância de guardaIdItem
        itemGuardado.setId(selectedValue);
        // Imprime para verificação
        System.out.println("Valor da primeira coluna: " + selectedValue);
 
        this.dispose();
    }//GEN-LAST:event_tblCadastroItemMouseClicked

