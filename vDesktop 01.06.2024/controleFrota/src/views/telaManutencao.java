    private void btnSalvarOSFinActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarOSFinActionPerformed
        ManutencaoDao dao = new ManutencaoDao();
        boolean statusPreventiva = false;
        boolean statusCorretiva = false;

        if (txtCodOSCorretFin.getText().isEmpty() && txtCodOSPrevFin.getText().isEmpty()) {
            lblFinalizarOS.setText("Código de OS não pode ser vazio.");
        } else if (!txtCodOSPrevFin.getText().isEmpty() && !rdbtn5Prev.isSelected()
                && !rdbtn10Prev.isSelected() && !rdbtn20Prev.isSelected()) {
            lblFinalizarOS.setText("Selecione uma porcentagem da OS Preventiva.");
        } else if (!txtCodOSCorretFin.getText().isEmpty() && !rdbtn5Corret.isSelected()
                && !rdbtn10Corret.isSelected() && !rdbtn20Corret.isSelected()) {
            lblFinalizarOS.setText("Selecione uma porcentagem da OS Corretiva.");
        } else {

            if (!txtCodOSPrevFin.getText().isEmpty()) {

                try {
                    Number valorNumero = formatoMoeda.parse(txtValorTotPrev.getText());
                    Double valorTotalPrev = valorNumero.doubleValue();
                    statusPreventiva = dao.atualizarOSPreventivaCorretiva(Integer.parseInt(txtCodOSPrevFin.getText()), valorTotalPrev);

                } catch (ParseException ex) {

                }

            }
            if (!txtCodOSCorretFin.getText().isEmpty()) {

                try {
                    Number valorNumero = formatoMoeda.parse(txtValorTotCorret.getText());
                    Double valorTotalPrev = valorNumero.doubleValue();
                    statusCorretiva = dao.atualizarOSPreventivaCorretiva(Integer.parseInt(txtCodOSCorretFin.getText()), valorTotalPrev);

                } catch (ParseException ex) {

                }

            }
            if (statusPreventiva == true && statusCorretiva == true) {
                lblFinalizarOS.setText("Manutenção Encerrada.");

            } else {

                if (statusPreventiva == true) {
                    lblFinalizarOS.setText("Manutenção Preventiva Encerrada.");

                }

                if (statusCorretiva == true) {
                    lblFinalizarOS.setText("Manutenção Corretiva Encerrada.");

                }
                if (statusPreventiva == false && statusCorretiva == false) {
                    lblFinalizarOS.setText("Não foi possivel encerrar a Manutenção.");

                }

            }

        }
         btnLimparPlacaActionPerformed(evt);
         btnLimparOSManutActionPerformed(evt);
          btnLimparProdActionPerformed(evt);
         
    }//GEN-LAST:event_btnSalvarOSFinActionPerformed

