    private void btnConsultarREActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarREActionPerformed

        String reConsulta;
        reConsulta = txtReConsulta.getText();
        UsuariosDao dao = new UsuariosDao();
        boolean status = dao.conectar();
        if (status == true) {
            usuarios = dao.consultar(reConsulta);
            System.out.println(" " + reConsulta);
            if (usuarios == null) {
                JOptionPane.showMessageDialog(null, "Funcionario não encontrado");
            } else {
                txtReCadastro.setText(usuarios.getRe());
                txtNome.setText(usuarios.getNome());
                txtEndereco.setText(usuarios.getEndereco());
                txtEmail.setText(usuarios.getEmail());
                txtTelefone.setText(usuarios.getTelefone());
                txtCpf.setText(usuarios.getCpf());
                txtSenha.setText(usuarios.getSenha());
                jCheckBox1.setSelected(usuarios.getInativarUsuario());
            }
        } else {
            JOptionPane.showMessageDialog(null, "Erro na conexão do banco de Dados");
        }

        dao.desconectar();

    }//GEN-LAST:event_btnConsultarREActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed

        UsuariosDao dao = new UsuariosDao();
        // Usuarios usuarios = new Usuarios();

        usuarios.setRe(txtReCadastro.getText());
        usuarios.setNome(txtNome.getText());
        usuarios.setEndereco(txtEndereco.getText());
        usuarios.setEmail(txtEmail.getText());
        usuarios.setTelefone(txtTelefone.getText());
        usuarios.setCpf(txtCpf.getText());
        usuarios.setSenha(txtSenha.getText());
        usuarios.setInativarUsuario(jCheckBox1.isSelected());

        boolean status = dao.conectar();
        if (status) {
            status = dao.alterar(usuarios);
            if (status) {
                JOptionPane.showMessageDialog(null, "Usuario Alterado com sucesso");
                limparCampos();

            } else {
                JOptionPane.showMessageDialog(null, "Usuário não foi alterado");
            }
            dao.desconectar();
        } else {
            JOptionPane.showMessageDialog(null, "Erro na conexão com o Banco de Dados");
        }

        dao.desconectar();
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
         UsuariosDao dao = new UsuariosDao();
        boolean status = dao.conectar();
        String reConsulta;
        reConsulta = txtReConsulta.getText();
        if (status == false) {
            JOptionPane.showMessageDialog(null, "Erro na conexão com o Banco de Dados");
        } else {
            if (status == true) {
                status = dao.excluir(reConsulta);
                JOptionPane.showMessageDialog(null, "\"Funcionário excluído com sucesso!!!");
                limparCampos();
                txtNome.requestFocus();
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao tentar excluir o funcionário");
            }
        }
        dao.desconectar();
    }//GEN-LAST:event_btnExcluirActionPerformed

