    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        UsuariosDao dao = new UsuariosDao();
        boolean status;
        int resp;
        Usuarios usuarios = new Usuarios();
        
        usuarios.setRe(txtReCadastro.getText());
        usuarios.setNome(txtNome.getText());
        usuarios.setEndereco((txtEndereco.getText()));
        usuarios.setEmail(txtEmail.getText());
        usuarios.setTelefone(txtTelefone.getText());
        usuarios.setCpf(txtCpf.getText());
        usuarios.setSenha(txtSenha.getText());
        usuarios.setConfirmarSenha(txtConfirmarSenha.getText());
        usuarios.setInativarUsuario(jCheckBox1.isSelected()); 

        status = dao.conectar();
        if (!status) {
            JOptionPane.showMessageDialog(null, "Erro na conexão com Banco do Dados");
        } else {
            resp = dao.salvar(usuarios);
            if (resp == 1) {
                JOptionPane.showMessageDialog(null, "Dados inseridos com sucesso!!!");
                limparCampos();
                txtNome.requestFocus();
            } else if (resp == 1062) {
                txtReConsulta.setText("");
                txtReCadastro.requestFocus();
                JOptionPane.showMessageDialog(null, "Numero do RE já cadastrado");

            } else {
                JOptionPane.showMessageDialog(null, "Erro ao salvar!!!");

            }

            dao.desconectar();

        }

    }//GEN-LAST:event_btnSalvarActionPerformed

