    public void Logar(String id , String senha){
        String dado = null;
        
            try {
            //seleciona o email quando for igual ao valor passado baseado no id 
            String sql = "SELECT email FROM usuario WHERE email = '" + id + "' ";            
            //Inicialização do banco de dados atravez do ResultSet
            Statement st = cn.createStatement();
            //Ele que executa a consulta no java
            ResultSet rs = st.executeQuery(sql);
            
            if(rs.first()){
                //seleciona a senha do usuario
                String sql1 = "SELECT senha FROM usuario WHERE senha = '" + senha + "' ";
                System.out.println("Senha" + sql1);
                //Toda a consulta gera esses dois Statement e ResultSet, não esquecer de sempre colocar um nome diferente da anterior
                Statement st1 = cn.createStatement();
                ResultSet rs1 = st1.executeQuery(sql1);
                
                if(rs1.next()){                    
                    //Fecha a janela atual(Login)
                    dispose();
                    //Chama a tela de Menu
                    telaInicial menu = new telaInicial();
                    menu.setLocationRelativeTo(null);
                    menu.setVisible(true);
     
                }else{
                     JOptionPane.showMessageDialog(this, "Senha Incorreta!", "Login", 0,
                    new ImageIcon(getClass().getResource("/Imagem/info.png")));
                }
                
            }else{
                JOptionPane.showMessageDialog(this, "O usuário não existe no banco de dados!", "Login", 0,
                    new ImageIcon(getClass().getResource("/Imagem/info.png")));
            }
                      
        } catch (Exception e) {
        }
    
    }    
    
