    private void rdbtn5PrevActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rdbtn5PrevActionPerformed
        //5% Preventiva
        ManutencaoDao dao = new ManutencaoDao();
        Double valorTotal = 0.0;
        
        if (txtCodOSPrevFin.getText() == null) {
            lblFinalizarOS.setText("Código da OS Preventiva não pode ser vazio.");
        } else {
            
            List<Manutencao> manutencaoPreventiva = dao.consultarPreventiva(Integer.parseInt(txtCodOSPrevFin.getText()));
            
            if (manutencaoPreventiva.isEmpty()) {
                lblFinalizarOS.setText("Nenhum informação encontrada na OS Preventiva.");
            } else {
                
                for (Manutencao manut : manutencaoPreventiva) {
                    valorTotal = valorTotal + manut.getValorProduto();
                }
                valorTotal = valorTotal + (valorTotal * 0.05);
                String valorFormatado = formatoMoeda.format(valorTotal);
                txtValorTotPrev.setText(valorFormatado);
            }
        }

    }//GEN-LAST:event_rdbtn5PrevActionPerformed

