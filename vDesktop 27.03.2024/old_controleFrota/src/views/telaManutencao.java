    private void btnConsultarPlacaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarPlacaActionPerformed
        
        if (txtPlacaManut.getText().isEmpty()) {
            lblConsultarOSManut.setText("A placa deve ser digitada.");
        } else {
            String placa = txtPlacaManut.getText();
            int value = 0;
            Connection cn = con.conexao();
            //Retornar as OS agendada referente essas placas
            try {
                cboCodOSCorretManut.removeAllItems();
                cboCodOSPreventManut.removeAllItems();
                cboCodOSCorretManut.addItem(null);
                cboCodOSPreventManut.addItem(null);
                
                PreparedStatement st = cn.prepareStatement("SELECT DISTINCT OSCorretiva FROM agenda WHERE placa = ? "
                        + "AND OSCorretiva IS NOT NULL AND OSCorretiva <> 0 AND status = ?");
                st.setString(1, placa);
                st.setString(2, "agendado");
                
                ResultSet rs = st.executeQuery();
                while (rs.next()) {
                    cboCodOSCorretManut.addItem(Integer.toString(rs.getInt("OSCorretiva")));
                    value = 1;
                }
                
                PreparedStatement stCorretiva = cn.prepareStatement("SELECT DISTINCT OSCorretiva FROM agenda WHERE placa = ? "
                        + "AND OSCorretiva IS NOT NULL AND OSCorretiva <> 0 AND status = ?");
                stCorretiva.setString(1, placa);
                stCorretiva.setString(2, "processo");
                
                ResultSet rsProcesso = stCorretiva.executeQuery();
                while (rsProcesso.next()) {
                    cboCodOSCorretManut.addItem(Integer.toString(rsProcesso.getInt("OSCorretiva")));
                    value = 1;
                }
                
                PreparedStatement stPreventiva = cn.prepareStatement("SELECT DISTINCT OSPreventiva FROM agenda WHERE placa = ? "
                        + "AND OSPreventiva IS NOT NULL AND OSPreventiva <> 0 AND status = ?");
                stPreventiva.setString(1, placa);
                stPreventiva.setString(2, "agendado");
                
                ResultSet rsResultPreventiva = stPreventiva.executeQuery();
                while (rsResultPreventiva.next()) {
                    cboCodOSPreventManut.addItem(Integer.toString(rsResultPreventiva.getInt("OSPreventiva")));
                    value = 1;
                }
                
                if (value == 0) {
                    lblConsultarOSManut.setText("Não tem OS cadastrada para essa placa.");
                } else {
                    lblConsultarOSManut.setText("Consulta realizada com sucesso, confira a OS disponivel referente o veiculo.");
                }
            } catch (SQLException ex) {
                lblConsultarOSManut.setText("Erro de banco, tente novamente.");
            }
            
        }
    }//GEN-LAST:event_btnConsultarPlacaActionPerformed

    private void btnConsultarOSManutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarOSManutActionPerformed
        ManutencaoDao dao = new ManutencaoDao();
        if (cboCodOSCorretManut.getSelectedItem() == null && cboCodOSPreventManut.getSelectedItem() == null) {
            
            lblConsultarOSPrevCorret.setText("Selecione pelo menos uma OS para a consulta.");
        } else {
            
            if (cboCodOSPreventManut.getSelectedItem() != null) {
                
                int codPreventiva = Integer.parseInt(cboCodOSPreventManut.getSelectedItem().toString());
                
                List<Manutencao> manutencaoPreventiva = dao.consultarPreventiva(codPreventiva);
                
                if (manutencaoPreventiva.isEmpty()) {
                    lblConsultarOSPrevCorret.setText("Nenhum informação encontrada na OS Preventiva.");
                } else {
                    DefaultTableModel model = new DefaultTableModel();
                    model.addColumn("CÓD. PROD.");
                    model.addColumn("KM MANUTENÇÃO");
                    model.addColumn("DESCRIÇÃO DO PRODUTO");
                    
                    for (Manutencao manut : manutencaoPreventiva) {
                        model.addRow(new Object[]{
                            manut.getCodProduto(),
                            manut.getKmManutencao(),
                            manut.getDescricaoProduto()
                        });
                    }
                    lblConsultarOSPrevCorret.setText("");
                    tblConsOSPrevent.setModel(model);
                }
                
            }
            if (cboCodOSCorretManut.getSelectedItem() != null) {
                
                int codCorretiva = Integer.parseInt(cboCodOSCorretManut.getSelectedItem().toString());
                
                List<CadastroItem> manutencaoCorretiva = dao.consultarCorretiva(codCorretiva);
                
                if (manutencaoCorretiva.isEmpty()) {
                    lblConsultarOSPrevCorret.setText("Nenhum informação encontrada na OS Corretiva.");
                } else {
                    DefaultTableModel model = new DefaultTableModel();
                    model.addColumn("CÓD. PROD.");
                    model.addColumn("DESCRIÇÃO DO PRODUTO");
                    
                    for (CadastroItem produto : manutencaoCorretiva) {
                        model.addRow(new Object[]{
                            produto.getIdCodigo(),
                            produto.getDescricaoItem()
                        
                        });
                    }
                    lblConsultarOSPrevCorret.setText("");
                    tblConsOSCorret.setModel(model);
                }
            }
            
        }
    }//GEN-LAST:event_btnConsultarOSManutActionPerformed

