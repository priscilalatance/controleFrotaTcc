    private void btnAdicionarProdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarProdActionPerformed
        ManutencaoDao manut = new ManutencaoDao();
        
        if (txtCodOSCorretAdd.getText() == null) {
            lblAdicionarProd.setText("Código da OS não pode ser vazio.");
        } else if (txtCodProd.getText() == null) {
            lblAdicionarProd.setText("Código do Produto não pode ser vazio.");
        } else if (manut.noEqualsProduct(txtCodProd.getText()) == 0) {
            lblAdicionarProd.setText("Esse código de produto não existe.");
        } else {
            boolean status = manut.updateProductOSCorretiva(Integer.parseInt(txtCodOSCorretAdd.getText()), txtCodProd.getText());
            
            if (status) {
                lblAdicionarProd.setText("Produto " + txtCodProd.getText() + " cadastrado com sucesso.");
            } else {
                lblAdicionarProd.setText("Produto " + txtCodProd.getText() + " não foi cadastrado");
            }
        }
    }//GEN-LAST:event_btnAdicionarProdActionPerformed

